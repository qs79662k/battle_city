	/**
	 * 初始化地图数据
	 * 1，jar所在目录中存在map文件，将使用map文件中的自定义地图
	 * 2，不存在map文件，或为无效地图数据时使用系统默认地图
	 */
	public static void init() {
		String path = new File("").getAbsolutePath() + "/map";
		File file = new File(path);
		//当自定义地图不存在时，使用系统自带地图
		if(!file.exists()) {
			return;
		}	
		//读取自定义地图
		RandomAccessFile in = null;
		try {
			in = new RandomAccessFile(file , "r");
			//如果不=0说明地图文件中不是有效地图数据
			long length = in.length();
			if(length % SIZE % SIZE != 0) {
				in.close();
				return;
			}
			//读取自定义地图数据
			maps = new byte[(int)length / SIZE / SIZE][26][26];
			for(int stage = 0 ; stage < maps.length ; stage++) {
				for(int y = 0 ; y < maps[stage].length ; y++) {
					in.read(maps[stage][y] , 0 , maps[stage][y].length);		
				}
			}
			in.close();
			isOldMap = false;
		} catch (Exception e) {
			e.printStackTrace();
		}
	}
	
	/**
	 * 存储自定义地图到指定关卡，当指定关卡大于现有最大关卡时将会做为其后续关卡
	 * @param minDecorateMap 小块帖图地图
	 * @param stage 关卡
	 */
	public static void saveMap(byte[][] minDecorateMap , int stage) {
		if(stage < 1) {
			stage = 1;
		} 
		byte[][] maxDecorateMap = convertMaxDecorateData(minDecorateMap, 26 , 26); //转换为大块帖图地图
		String path = new File("").getAbsolutePath() + "/map"; //获取项目所在目录(jar所在目录)
		File file = new File(path);
		RandomAccessFile os = null;
		try {
			os = new RandomAccessFile(file , "rw");
			long length = os.length();
			long pos = stage * SIZE * SIZE - SIZE * SIZE;	
			if(pos > length) {
				pos = length;
			}
			os.seek(pos);		
			for(int y = 0 ; y < maxDecorateMap.length ; y++) {
				for(int x = 0 ; x < maxDecorateMap[y].length ; x++) {
					os.write(maxDecorateMap[y][x]);
				}
			}
			os.close();
		} catch (Exception e) {
			e.printStackTrace();
		}
	}
	
	/**
	 * 存储多个地图，如果已存在自定义地图将会被覆盖
	 * @param minDecorateMaps 多个小块帖图地图
	 */
	public static void saveMaps(byte[][][] minDecorateMaps) {
		byte[][][] maxDecorateMaps = new byte[minDecorateMaps.length][][];
		for(int stage = 0 ; stage < maxDecorateMaps.length ; stage++) {
			maxDecorateMaps[stage] = convertMaxDecorateData(minDecorateMaps[stage] , 26, 26);
		}
		String path = new File("").getAbsolutePath() + "/map";
		File file = new File(path);
		BufferedOutputStream os = null;
		try {
			os = new BufferedOutputStream(new FileOutputStream(file));
			for(int stage = 0 ; stage < maxDecorateMaps.length ; stage++) {
				for(int y = 0 ; y < maxDecorateMaps[stage].length ; y++) {
					for(int x = 0 ; x < maxDecorateMaps[stage][y].length ; x++) {
						os.write(maxDecorateMaps[stage][y][x]);
					}
				}
			}
			os.flush();
			os.close();
		} catch (Exception e) {
			e.printStackTrace();
		}
	}
	
