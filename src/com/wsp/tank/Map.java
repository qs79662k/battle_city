	private final static int DECORATE_LENGTH = 44;
	
	private static byte[][] convertMinDecorateData(byte[][] maxDecorateData , int width , int height) {
		byte[][] minDecorateData = new byte[height][width];
		for(int y = 0 ; y < maxDecorateData.length ; y++) {
			for(int x = 0 ; x < maxDecorateData[y].length ; x++) {
				int decorate = maxDecorateData[y][x] * 2;		
				if(decorate != 0) {
					int _y = decorate / DECORATE_LENGTH;
					int _x = decorate % DECORATE_LENGTH;
					minDecorateData[y * 2][x * 2] = (byte)(_x * 4  + _y);	//左上
					minDecorateData[y * 2][x * 2 + 1] = (byte)((_x + 1) * 4  + _y);	//右上
					minDecorateData[y * 2 + 1][x * 2] = (byte)(_x * 4  + _y + 1);	//左下
					minDecorateData[y * 2 + 1][x * 2 + 1] = (byte)((_x + 1) * 4  + _y + 1);	//右下
				}				
			}
		}
		return minDecorateData;	
	}
	
	private static byte[][] convertMaxDecorateData(byte[][] minDecorateData , int width , int height) {
		byte[][] maxDecorateData = new byte[width][height];
		for(int y = 0 ; y < maxDecorateData.length ; y++) {
			for(int x = 0 ; x < maxDecorateData[y].length ; x++) {
				int minDecorate = minDecorateData[y * 2][x * 2];
				int _x = minDecorate / 4;
				int _y = minDecorate % 4;
				int maxDecorate = (_y * DECORATE_LENGTH  + _x) / 2;
				maxDecorateData[y][x] = (byte)maxDecorate;
			}
		}
		return maxDecorateData;
	}
	
